package br.com.desafio.algorithms.dto;

public class NumerosMultiplosDto {

    private int a;
    private int b;

    public NumerosMultiplosDto(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return this.a;
    }

    public int getB() {
        return this.b;
    }
}